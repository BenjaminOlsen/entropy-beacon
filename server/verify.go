package main

import "crypto/ed25519"

// Beacon's Ed25519 public key, derived from the dev seed in firmware/src/main.c.
// TODO: load from config file once per-device provisioning is in place.
var beaconPublicKey = ed25519.PublicKey{
	0x79, 0xb5, 0x56, 0x2e, 0x8f, 0xe6, 0x54, 0xf9,
	0x40, 0x78, 0xb1, 0x12, 0xe8, 0xa9, 0x8b, 0xa7,
	0x90, 0x1f, 0x85, 0x3a, 0xe6, 0x95, 0xbe, 0xd7,
	0xe0, 0xe3, 0x91, 0x0b, 0xad, 0x04, 0x96, 0x64,
}

func verifySignature(entropy, signature []byte) bool {
	return ed25519.Verify(beaconPublicKey, entropy, signature)
}
